from whalesbot import *

# =============================================================================
# ★★★ 대회장에서 수정할 값들 ★★★
# =============================================================================
# 각 장애물 근처 바닥 태그 번호 (대회장에서 확인 후 입력)
# TAG_RING1 = 0       # 링 통과 1 근처 태그
# TAG_RING2 = 0       # 링 통과 2 근처 태그
# TAG_PILLAR = 0      # 기둥 장애물 근처 태그
# TAG_SBAR = 0        # S자 가로봉 근처 태그
# TAG_DBAR = 0        # 이중 가로봉 근처 태그
# TAG_HRING = 0       # 수평 링 근처 태그
# TAG_OBSTACLE = 0    # 장애물 회피 근처 태그
# TAG_HOME = 0        # 출발/착륙 지점 태그
# FLIGHT_SPEED = 30   # AI_GoToTag 속도 (20~40 권장)

# =============================================================================
# 초기화
# =============================================================================
resettime()
fly_unlock()
wait(1)
fly_setspeed(30)
wait(1)

# =============================================================================
# 이륙
# =============================================================================
fly_start(80)
wait(3)
fly_hover()
wait(2)

# =============================================================================
# AI 태그맵 초기화 - 바닥 태그 인식 시작
# =============================================================================
AI_TagMapInit(P1, AI_get(P1, AIMODE_APILTAG, 0), 30)
wait(3)

# =============================================================================
# 미션 1: 태그 20번으로 이동 -> 링 통과
# =============================================================================

# 태그 20번 위치로 이동
AI_GoToTag(20, 30)
wait(5)
fly_hover()
wait(2)

# 링 통과 (전진)
fly_moveto(FRONT, 80)
wait(3)
fly_hover()
wait(2)

# =============================================================================
# 착륙
# =============================================================================
fly_land()
wait(3)
fly_lock()
