"""
=============================================================================
대회 당일 퀵 설정 가이드
=============================================================================

이 파일은 대회 당일 드론 SDK가 확정된 후,
메인 코드(enjoy_ai_drone_mission.py)를 빠르게 커스터마이징하는 가이드입니다.

=============================================================================
[STEP 1] 드론 SDK 확인 및 import 수정
=============================================================================

드론: 가이온에듀테크 이글드론 (BYROBOT e-drone SDK)

■ 확정된 SDK: e-drone (pip install e-drone / uv add e-drone)
  - 연결: PC ↔ USB 시리얼 ↔ 조종기 ↔ 무선 ↔ 드론
  - import 예시:
      from e_drone.drone import *
      from e_drone.protocol import *

      drone = Drone()
      drone.open()                    # 시리얼 연결
      drone.send_takeoff()            # 이륙
      drone.send_control(0,0,0,0)     # 호버링
      drone.send_control_position(1.0, 0, 0, 0.5, 0, 0)  # 1m 전진 (0.5m/s)
      drone.send_control_position(0, 0, 0.5, 0.3, 0, 0)  # 0.5m 상승
      drone.send_control_position(0, 0, 0, 0, 90, 30)     # 시계방향 90도 회전
      drone.send_landing()            # 착륙
      drone.close()                   # 연결 종료

  ※ send_control_position(x, y, z, speed, yaw, yaw_speed)
      x: 전후 (m, +전진/-후진)
      y: 좌우 (m, +좌/-우)
      z: 상하 (m, +상승/-하강)
      speed: 이동속도 (m/s)
      yaw: 회전각도 (도, +시계/-반시계)
      yaw_speed: 회전속도 (도/s)


=============================================================================
[STEP 2] 장애물 좌표 측정 (워밍업 시간에 반드시 수행!)
=============================================================================

필드 입장 후 각 장애물의 실제 좌표를 눈으로 확인하고,
FieldNavigator.OBSTACLES 딕셔너리를 수정하세요.

측정 팁:
  - 필드는 300×300cm, 좌측 하단이 (0,0)
  - QR코드가 20×20cm 간격으로 배치되어 있으므로 참고
  - 드론 베이스는 좌측 하단 30×30cm 영역

장애물 높이도 확인:
  - 낮은 링 vs 높은 링의 실제 높이차
  - 가로봉의 실제 높이
  - 수평 링의 설치 높이


=============================================================================
[STEP 3] 전략 선택
=============================================================================

■ "default" (고배점 우선) - 자신감 있을 때
  이중가로봉(180) → 링(100) → 장애물(100) → S자(80) → 수평링(70) → 기둥(60)
  최대 점수: 720점

■ "safe" (안전 우선) - 처음 출전하거나 드론이 불안정할 때
  링(100) → 기둥(60) → 장애물(100) → S자(80) → 수평링(70) → 이중가로봉(180)
  쉬운 과제부터 해서 확실한 점수 확보

■ "speed" (최단경로) - 시간이 부족할 것 같을 때
  가까운 장애물부터 순서대로 수행


=============================================================================
[STEP 4] 비행 파라미터 미세 조정
=============================================================================

대회장 환경(조명, 바닥재질, 기류 등)에 따라 아래 값을 조정하세요:

# 속도 조정 (너무 빠르면 QR 인식 실패, 너무 느리면 시간 초과)
MOVE_SPEED = 30      # cm/s (기본값, 20~40 범위에서 조정)
ROTATION_SPEED = 30  # deg/s

# 호버링 대기 시간 (안정화)
HOVER_TIME = 0.5     # 초 (드론이 흔들리면 늘리기)

# 원호 비행 분해 단위
ARC_STEP_DEGREES = 30  # 작을수록 정밀하지만 느림

# 복귀 예비 시간
RETURN_BUFFER = 20     # 초 (필드가 크면 늘리기)


=============================================================================
[STEP 5] 테스트 체크리스트
=============================================================================

□ 드론 연결 확인
□ 배터리 잔량 확인 (풀 충전 권장)
□ 이륙 → 호버링 → 착륙 기본 테스트
□ 전진/후진/좌/우 이동 테스트
□ 회전 정확도 테스트 (90도 회전 후 실제 각도 확인)
□ QR코드 인식 테스트
□ 장애물 좌표 입력 완료
□ 전체 미션 시뮬레이션 (드론 없이 코드만 실행)
□ 프로펠러 보호대 장착 확인
□ 드론 잠금/해제 방법 설정 확인


=============================================================================
[주의사항] 실격 방지 체크리스트
=============================================================================

✗ 온라인 접속, 다운로드 절대 금지 → 실격
✗ 필드 사진/비디오 촬영 금지 → 실격
✗ 지도교사/부모님과 대화 금지 → 실격 (심판장 승인 없이)
✗ 드론 비행 중 필드 안 진입 금지 → 실격
✗ 심판 허가 없이 드론 수리/조정 금지 → 실격
✗ "시작!" 전에 드론 비행 → 1회 경고, 2회 실격

○ 지각 시 1분당 -10점, 10분 이상 시 실격
○ 구조물 파손 시 경고 + 해당 과제 점수 무효
"""
